% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% [filename, pathname, filterindex] = uigetfile('*.jpg', 'Pick a MATLAB code file');
% a=imread([pathname filename]);
 %imshow(a)
  %h = imrect;
  %position = wait(h);
 % handles.a=handles.a(position(2):position(2)+position(4),position(1):position(1)+position(3),:);
  %image(handles.a)
  r_min_norm=str2num(get(handles.edit3,'string'));
  r_max_norm=str2num(get(handles.edit4,'string'));
  g_min_norm=str2num(get(handles.edit2,'string'));
  g_max_norm=str2num(get(handles.edit5,'string'));
  b_min_norm=str2num(get(handles.edit6,'string'));
  b_max_norm=str2num(get(handles.edit7,'string'));

  r_min_mild=str2num(get(handles.edit26,'string'));
  r_max_mild=str2num(get(handles.edit27,'string'));
  g_min_mild=str2num(get(handles.edit28,'string'));
  g_max_mild=str2num(get(handles.edit29,'string'));
  b_min_mild=str2num(get(handles.edit30,'string'));
  b_max_mild=str2num(get(handles.edit31,'string'));
  
  r_min_mod=str2num(get(handles.edit20,'string'));
  r_max_mod=str2num(get(handles.edit21,'string'));
  g_min_mod=str2num(get(handles.edit22,'string'));
  g_max_mod=str2num(get(handles.edit23,'string'));
  b_min_mod=str2num(get(handles.edit24,'string'));
  b_max_mod=str2num(get(handles.edit25,'string'));
  
  r_min_sev=str2num(get(handles.edit37,'string'));
  r_max_sev=str2num(get(handles.edit36,'string'));
  g_min_sev=str2num(get(handles.edit35,'string'));
  g_max_sev=str2num(get(handles.edit34,'string'));
  b_min_sev=str2num(get(handles.edit33,'string'));
  b_max_sev=str2num(get(handles.edit32,'string'));
  
  normR1=handles.a(:,:,1)<=r_max_norm;
  normR2=handles.a(:,:,1)>=r_min_norm;
  normR=normR1 & normR2;
  normG1=handles.a(:,:,2)<=g_max_norm;
  normG2=handles.a(:,:,2)>=g_min_norm;
  normG=normG1 & normG2;
  normB1=handles.a(:,:,3)<=b_max_norm;
  normB2=handles.a(:,:,3)>=b_min_norm;
  normB=normB1 & normB2;  
  norm=normR & normG & normB;
  norm_pix=sum(sum(norm));
   
  mildR1=handles.a(:,:,1)<=r_max_mild;
  mildR2=handles.a(:,:,1)>=r_min_mild;
  mildR=mildR1 & mildR2;
  mildG1=handles.a(:,:,2)<=g_max_mild;
  mildG2=handles.a(:,:,2)>=g_min_mild;
  mildG=mildG1 & mildG2;
  mildB1=handles.a(:,:,3)<=b_max_mild;
  mildB2=handles.a(:,:,3)>=b_min_mild;
  mildB=mildB1 & mildB2;
  mild=mildR & mildG & mildB;
  mild_pix=sum(sum(mild));

  modR1=handles.a(:,:,1)<=r_max_mod;
  modR2=handles.a(:,:,1)>=r_min_mod;
  modR=modR1 & modR2;
  modG1=handles.a(:,:,2)<=g_max_mod;
  modG2=handles.a(:,:,2)>=g_min_mod;
  modG=modG1 & modG2;
  modB1=handles.a(:,:,3)<=b_max_mod;
  modB2=handles.a(:,:,3)>=b_min_mod;
  modB=modB1 & modB2;
  mod=modR & modG & modB;
  mod_pix=sum(sum(mod));
  
  sevR1=handles.a(:,:,1)<=r_max_sev;
  sevR2=handles.a(:,:,1)>=r_min_sev;
  sevR=sevR1 & sevR2;
  sevG1=handles.a(:,:,2)<=g_max_sev;
  sevG2=handles.a(:,:,2)>=g_min_sev;
  sevG=sevG1 & sevG2;
  sevB1=handles.a(:,:,3)<=b_max_sev;
  sevB2=handles.a(:,:,3)>=b_min_sev;
  sevB=sevB1 & sevB2;
  sev=sevR & sevG & sevB;
  sev_pix=sum(sum(sev));
  
  tot=norm_pix+mild_pix+mod_pix+sev_pix;
  norm_percent=(norm_pix/tot)*100;
  mild_percent=(mild_pix/tot)*100;
  mod_percent=(mod_pix/tot)*100;
  sev_percent=(sev_pix/tot)*100;

  
  ss=handles.a;
  
  [g,h]=find(norm==1);
  [o,p]=find(mild==1);
  [k,l]=find(mod==1);
  [m,n]=find(sev==1);
  for i=1:length(g)
  ss(g(i),h(i),:)=240;
  end
 
  
  
  
  
  
 for ii=1:length(o)
 ss(o(ii),p(ii),1)=255;
 ss(o(ii),p(ii),3)=255;
  ss(o(ii),p(ii),2)=0;
 
 
  end
 % 
 for iii=1:length(k)
 ss(k(iii),l(iii),[2:3])=255;
  ss(k(iii),l(iii),1)=0;
  end
  
 for iiii=1:length(m)
 ss(m(iiii),n(iiii),[1:2])=255;
 ss(m(iiii),n(iiii),[3])=0;
  end 
  
  image(ss)
  
  
  
  
 set(handles.text32,'string',num2str(norm_percent));  
 set(handles.text33,'string',num2str(mild_percent));  
 set(handles.text34,'string',num2str(mod_percent));  
 set(handles.text35,'string',num2str(sev_percent));  

guidata(hObject, handles);


